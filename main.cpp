#include <iostream>

using namespace std;

int main()
{
    long long firstNum = 0, secondNum = 0;
    cin >> firstNum >> secondNum;
    cout << firstNum << " + " << secondNum << " = " << firstNum + secondNum << endl;
    cout << firstNum << " * " << secondNum << " = " << firstNum * secondNum << endl;
    cout << firstNum << " - " << secondNum << " = " << firstNum - secondNum << endl;
    return 0;
}
